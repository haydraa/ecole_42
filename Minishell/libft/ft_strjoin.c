#include "libft.h"

char	*ft_strjoin(char *s1, char *s2)
{
	char	*str;
	size_t	i;
	size_t	j;

	i = -1;
	j = -1;
	str = (char *)malloc(ft_strlen(s1) + ft_strlen(s2) + 1);
	if (!str)
		return (NULL);
	if (s1)
		while (s1[++i])
			str[i] = s1[i];
	else
		i = 0;
	if (s2)
		while (s2[++j])
			str[i + j] = s2[j];
	else
		j = 0;
	str[i + j] = '\0';
	return (str);
}
