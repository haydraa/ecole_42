#include "libft.h"

void	*ft_bzero(void *s, size_t i)
{
	unsigned char	*p;
	int				y;

	y = 0;
	p = s;
	while (i > 0)
	{
		p[y] = '\0';
		i--;
		y++;
	}
	return (s);
}
